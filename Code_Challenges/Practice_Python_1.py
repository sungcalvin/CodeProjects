import csv

#Collects dates and colors from csv file, then prints out list of all dates and colors.
with open('example.csv') as csvfile:
    readCSV = csv.reader(csvfile, delimiter=',')
    dates = []
    colors = []
    for row in readCSV:
        color = row[3]
        date = row[0]

        dates.append(date)
        colors.append(color)

print(dates)
print(colors)

